import DataBaseYGO

class BDInterface(ABC):

    @abstractclassmethod
    def insertarMain():
        pass

    @abstractclassmethod
    def VisualizarMain():
        pass
    
    @abstractclassmethod
    def insertarExtra():
        pass

    @abstractclassmethod
    def VisualizarExtra():
        pass

    @abstractclassmethod
    def insertarSide():
        pass

    @abstractclassmethod
    def VisualizarSide():
        pass

